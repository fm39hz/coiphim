            $.fn.imagesLoaded = function() {
                var $imgs = this.find('img[src!=""]');
                if (!$imgs.length) {
                    return $.Deferred().resolve().promise();
                }
                var dfds = [];
                $imgs.each(function() {
                    var dfd = $.Deferred();
                    dfds.push(dfd);
                    var img = new Image();
                    img.onload = function() {
                        dfd.resolve();
                    };
